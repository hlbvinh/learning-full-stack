##checking data type

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex
print(type('Asabeneh'))          # String
print(type([1, 2, 3]))           # List 
print(type({'name':'Asabeneh'})) # Dictionary
print(type({9.8, 3.14, 2.7}))    # Set Là tập hợp các giá trị duy nhất, không có thứ tự.
print(type((9.8, 3.14, 2.7)))    # Tuple Là tập hợp các giá trị có thứ tự, không thay đổi.